#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int	comp_affi(char str)
{
	if (str < 32 || str == 127)
		return (0);
	else
		return (1);
}

void	ft_putstr_non_printable(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (comp_affi(str[i]) == 1)
			ft_putchar(str[i]);
		else
		{
			ft_putchar('\\');
			ft_putchar("0123456789abcdef"[str[i] / 16]);
			ft_putchar("0123456789abcdef"[str[i] % 16]);
		}
		i ++;
	}
}
