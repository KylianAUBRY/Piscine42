int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	i;

	i = 0;
	while (s1[i] == s2[i] && n -1 > 0)
	{
		i ++;
		n --;
	}
	if (s1[i - 1] == '\0' && s2 [i - 1] == '\0' && (i - 1) > 0)
	{
		return (0);
	} else
	{
		return (s1[i] - s2[i]);
	}
}
